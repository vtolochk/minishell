#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t i;

	i = 0;
	while (*src != '\0')
	{
		i++;
		*dst++ = *src++;
	}
	*dst = '\0';
	return (dst - i);
}
