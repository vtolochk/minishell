#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	size_t i;

	i = ft_strlen(s1) + n;
	while (*s1 != '\0')
		s1++;
	while (n-- != 0 && *s2 != '\0')
		*s1++ = *s2++;
	*s1 = '\0';
	return (s1 - i);
}
